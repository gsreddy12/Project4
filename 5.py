#Non primitive Data Structures

#List

a='PYTHON'
print(type(a))
print(a)
b=list(a)
print(type(b))
print(b)

#Tuple

a='PYTHON'
print(type(a))
print(a)
b=tuple(a)
print(type(b))
print(b)

#set

a='PYTHON'
print(type(a))
print(a)
b=set(a)
print(type(b))
print(b)

#Dict(key,value)

MyTup=(('a',1),('b',2),('c',3))
print(type(MyTup))
print(MyTup)
MyDict=dict(MyTup)
print(type(MyDict))
print(MyDict)

